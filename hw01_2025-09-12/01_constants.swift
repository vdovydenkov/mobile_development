// Объявляем константу firstString со значением "I can"
let firstString = "I can"

// Объявляем константу secondString со значением "code"
let secondString = "code"

// Теперь нам нужно вывести на консоль фразу "I can code!".
// Для этого объединим значения констант с пробелом между ними
// и добавим восклицательный знак в конце.
let result = firstString + " " + secondString + "!"

// Выводим получившуюся строку на консоль
print(result)
