import Foundation // Импортируем модуль Foundation, чтобы использовать функцию sqrt()

// Задаем длину катетов прямоугольного треугольника
let AC = 8.0
let CB = 6.0

// Вычисляем гипотенузу AB по теореме Пифагора
// Квадрат катета получаем умножением его на самого себя (pow использовать нельзя)
let AB = sqrt(AC * AC + CB * CB)

// Вычисляем площадь прямоугольного треугольника
let area = (AC * CB) / 2

// Вычисляем периметр прямоугольного треугольника
let perimeter = AC + CB + AB

// Выводим результаты
print("Гипотенуза AB = \(AB)")
print("Площадь треугольника = \(area)")
print("Периметр треугольника = \(perimeter)")
